// Promise is a powerful tool for handling asynchronous operations
// It represents the eventual completion (or failure) of an asynchronous operation and its resulting value. 
// Promises provide a more manageable and readable approach compared to traditional callback functions, 
// especially when dealing with multiple asynchronous tasks.


myPromise.then((message) => {
        console.log(message); 
    })
    .catch((error) => {
        console.error(error); 
    });
